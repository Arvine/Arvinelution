function c51100236.egpop(e,tp,eg,ep,ev,re,r,rp)
	local c=e:GetHandler()
	if c:IsRelateToEffect(e) and c:IsFaceup() then
		local e1=Effect.CreateEffect(c)
		e1:SetType(EFFECT_TYPE_SINGLE)
		e1:SetCode(EFFECT_DISABLE)
		e1:SetReset(RESET_EVENT+0x1fe0000+RESET_PHASE+PHASE_END)
		c:RegisterEffect(e1)
		local e2=Effect.CreateEffect(c)
		e2:SetType(EFFECT_TYPE_SINGLE)
		e2:SetCode(EFFECT_DISABLE_EFFECT)
		e2:SetReset(RESET_EVENT+0x1fe0000+RESET_PHASE+PHASE_END)
		c:RegisterEffect(e2)
		Duel.BreakEffect()
		Duel.SendtoGrave(c,REASON_EFFECT)
		if Duel.GetLocationCount(tp,LOCATION_MZONE)<1 then return end
		if not Duel.IsPlayerCanSpecialSummonMonster(tp,511000237,0,0x21,0,0,10,RACE_DEVINE,ATTRIBUTE_DEVINE) then return end
		for i=1,1 do
		local token=Duel.CreateToken(tp,511000236+i)
		Duel.SpecialSummonStep(token,0,tp,tp,false,false,POS_FACEUP_ATTACK)
		local e3=Effect.CreateEffect(e:GetHandler())
		e3:SetType(EFFECT_TYPE_SINGLE)
		e3:SetProperty(EFFECT_FLAG_SINGLE_RANGE)
		e3:SetRange(LOCATION_MZONE)
		e3:SetCode(EFFECT_INDESTRUCTABLE_BATTLE)
		e3:SetValue(1)
		e3:SetReset(RESET_EVENT+0x1ff0000+RESET_PHASE+PHASE_END)
		token:RegisterEffect(e3,true)
		local e4=e3:Clone()
		e4:SetCode(EFFECT_AVOID_BATTLE_DAMAGE)
		token:RegisterEffect(e4,true)
		local e5=e3:Clone()
		e5:SetCode(EFFECT_IMMUNE_EFFECT)
		token:RegisterEffect(e5,true)
		local e6=Effect.CreateEffect(e:GetHandler())
		e6:SetType(EFFECT_TYPE_FIELD+EFFECT_TYPE_CONTINUOUS)
		e6:SetProperty(EFFECT_FLAG_CANNOT_DISABLE)
		e6:SetRange(LOCATION_MZONE)
		e6:SetCode(EVENT_PHASE+PHASE_END)
		e6:SetCountLimit(1)
		e6:SetOperation(c51100236.egpendop)
		token:RegisterEffect(e6,true)
		--Incinerate
		local e7=Effect.CreateEffect(e:GetHandler())
		e7:SetDescription(aux.Stringid(51100236,0))
		e7:SetCategory(CATEGORY_DISABLE+CATEGORY_DESTROY)
		e7:SetProperty(EFFECT_FLAG_UNCOPYABLE+EFFECT_FLAG_IGNORE_IMMUNE)
		e7:SetType(EFFECT_TYPE_QUICK_O)
		e7:SetCode(EVENT_FREE_CHAIN)
		e7:SetHintTiming(TIMING_DAMAGE_STEP)
		e7:SetRange(LOCATION_MZONE)
		e7:SetCountLimit(1)
		e7:SetCost(c51100236.inccost)
		e7:SetTarget(c51100236.inctg)
		e7:SetOperation(c51100236.incop)
		token:RegisterEffect(e7)
	end
	Duel.SpecialSummonComplete()
end
