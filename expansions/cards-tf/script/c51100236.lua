function c51100236.costfilter(c)
	return c:IsCode(95286165) and c:IsAbleToGraveAsCost()
end
function c51100236.defusicost(e,tp,eg,ep,ev,re,r,rp,chk)
		if chk==0 then return Duel.IsExistingMatchingCard(c51100236.costfilter,tp,LOCATION_HAND+LOCATION_SZONE,0,1,e:GetHandler()) and Duel.GetFlagEffect(tp,51100237)==1 
		and bit.band(e:GetHandler():GetSummonType(),SUMMON_TYPE_SPECIAL)==SUMMON_TYPE_SPECIAL and e:GetHandler():GetAttack()>0 end
		Duel.Hint(HINT_SELECTMSG,tp,HINTMSG_TOGRAVE)
		local g=Duel.SelectMatchingCard(tp,c51100236.costfilter,tp,LOCATION_HAND+LOCATION_SZONE,0,1,1,nil)
		Duel.SendtoGrave(g,REASON_COST)
end
function c51100236.defusiop(e,tp,eg,ep,ev,re,r,rp)
	local c=e:GetHandler()
	if c:IsFaceup() and c:IsRelateToEffect(e) then
		local ATK=c:GetAttack()
		local DEF=c:GetDefence()
		Duel.Recover(tp,ATK,REASON_EFFECT)
		local e1=Effect.CreateEffect(e:GetHandler())
		e1:SetType(EFFECT_TYPE_SINGLE)
		e1:SetReset(RESET_EVENT+0x1fe0000)
		e1:SetCode(EFFECT_SET_BASE_ATTACK)
		e1:SetValue(-ATK)
		c:RegisterEffect(e1)
		local e2=e1:Clone()
		e2:SetCode(EFFECT_SET_BASE_DEFENCE)
		e2:SetValue(-DEF)
		c:RegisterEffect(e2)
		end
		if Duel.GetAttacker()==e:GetHandler() then
		Duel.NegateAttack()
	end
end
